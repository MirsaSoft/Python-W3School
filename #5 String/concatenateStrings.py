# PENGGABUNGAN STRING

# Untuk menggabungkan, atau mengombinasikan, dua string, anda dapat menggunakan operator +.

# Gabungkan variabel a dengan variabel b ke dalam variabel c :

a = "Hello"
b = "World"
c = a + b
print(c)

print("===========================")

# Untuk menambahkan spasi di antara keduanya, tambahkan " " :

a = "Hello"
b = "World"
c = a + " " + b
print(c)
