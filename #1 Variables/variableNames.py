# VARIABLE NAMES
# Suatu variabel dapat memiliki nama yang pendek (seperti x dan y) atau nama yang lebih deskriptif (age, carname, total_volume). Aturan untuk variabel Python :

# Nama variabel harus dimulai dengan huruf atau karakter garis bawah
# Nama variabel tidak dapat dimulai dengan angka
# Nama variabel hanya dapat berisi karakter alfanumerik dan garis bawah (Az, 0-9, dan _)
# Nama variabel peka huruf besar/kecil (Age, Age, dan AGE adalah tiga variabel berbeda)
# Nama variabel tidak dapat berupa salah satu kata kunci python

# NAMA VARIABEL MULTI KATA
# Nama variabel dengan lebih dari satu kata dapat sulit dibaca. Ada beberapa teknik yang dapat anda gunakan untuk membuatnya lebih mudah dibaca :

# CAMEL CASE
# Setiap kata, kecuali yang pertama, dimulai dengan huruf kapital :

myVariableName = "John"

# PASCAL CASE
# Setiap kata dimulai dengan huruf kapital :

MyVariableName = "John"

# SNAKE CASE
# Setiap kata dipisahkan oleh karakter garis bawah :

my_variable_name = "John"